package br.com.cesed.bd2.petshop.DTO.Funcionario;

public class FuncionarioComissaoObje {

	private int matricula;
	private int mes;
	private int ano;

	public int getMatricula() {
		return matricula;
	}

	public void setMatricula(int matricula) {
		this.matricula = matricula;
	}

	public int getMes() {
		return mes;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}

	public int getAno() {
		return ano;
	}

	public void setAno(int ano) {
		this.ano = ano;
	}

}
