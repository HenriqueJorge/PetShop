package br.com.cesed.bd2.petshop.DTO.VendaAnimal;

public class VendaAnimalRelatorioFuncionario {

	private int mes;
	private int ano;
	private String tipo;

	public int getMes() {
		return mes;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}

	public int getAno() {
		return ano;
	}

	public void setAno(int ano) {
		this.ano = ano;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

}
